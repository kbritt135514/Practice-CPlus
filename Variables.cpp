#include <iostream>

using namespace std;

int main() {

    int Integers; 
    // stores whole numbers(NO DECIMALS)

    double Floaters;
    // Stores floating point number(WITH DECIMALS)

    char Characters;
    // Stores SINGULAR letters surrounded by SINGLE QUOTES

    string Strings;
    // Stores text.

    bool TF;
    // Stores two values, true or false


    // Assigning Data
    Integers = 45;

    Floaters = 4.5;

    Characters = 'T';

    Strings = "This is a string.";

    TF = true;


    //Outputing Data

    cout <<"This is the Integer: "<< Integers <<endl;

    cout <<"This is a Float/Decimal: "<< Floaters<< endl;

    cout <<"This is a single character: "<< Characters<< endl;

    cout <<"This is a string phrase: "<< Strings<< endl;

    cout <<"This is boolean Value: "<< TF<< endl;


    cout<<"Thank you for your time, good bye."<<endl;;

    return 0;


}